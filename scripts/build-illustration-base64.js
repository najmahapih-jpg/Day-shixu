const fs = require('fs');
const path = require('path');

const staticImagesDir = path.join(__dirname, '../static/images');
const outputFile = path.join(__dirname, '../utils/illustration-map.js');

function scanDir(dir) {
    let results = [];
    const list = fs.readdirSync(dir);

    for (const file of list) {
        const fullPath = path.join(dir, file);
        const stat = fs.statSync(fullPath);

        if (stat && stat.isDirectory()) {
            results = results.concat(scanDir(fullPath));
        } else {
            if (file.endsWith('.svg')) {
                results.push(fullPath);
            }
        }
    }
    return results;
}

function buildMap() {
    console.log('🖼️ Building illustration Base64 map...');
    const svgFiles = scanDir(staticImagesDir);
    const map = {};

    for (const filePath of svgFiles) {
        // Determine object key: e.g. "custom/illustrations/character-morning"
        const relativePath = path.relative(staticImagesDir, filePath);
        // Use forward slashes for cross-platform keys
        const key = relativePath.replace(/\\/g, '/').replace('.svg', '');

        // Read and encode
        let svgContent = fs.readFileSync(filePath, 'utf-8');
        // Remove formatting whitespace for smaller base64 if desired, or just raw encode
        const base64 = Buffer.from(svgContent).toString('base64');

        map[key] = `data:image/svg+xml;base64,${base64}`;
        console.log(`✅ Indexed: ${key}`);
    }

    const outputContent = `// Auto-generated by build-illustration-base64.js — DO NOT EDIT
// Generated at: ${new Date().toISOString()}
export default ${JSON.stringify(map, null, 2)};
`;

    fs.writeFileSync(outputFile, outputContent);
    console.log(`\n🎉 Success! Wrote ${svgFiles.length} illustrations to ${outputFile}`);
}

buildMap();
